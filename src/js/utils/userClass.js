export class User {
  constructor() {
    this.mail = '';
    this.idToken = '';
    this.localId = '';
    this.isLogin = false;
  }

  userLogin(mail, idToken, localId) {
    this.mail = mail;
    this.idToken = idToken;
    this.localId = localId;
    this.isLogin = true;
  }

  userLogOut() {
    this.mail = '';
    this.idToken = '';
    this.localId = '';
    this.isLogin = false;
  }
}
